package Week3;

public class Calculator2 {

    public void plus(int a, int b){
        int result = a+b;
        System.out.println(result);
    }

    public void minus(int a, int b){
        int result = a-b;
        System.out.println(result);
    }

    public void multiple(int a, int b){
        int result = a*b;
        System.out.println(result);
    }

    public void divide(int a, int b){
        float result = (float) a/b;
        System.out.println(result);
    }

}
